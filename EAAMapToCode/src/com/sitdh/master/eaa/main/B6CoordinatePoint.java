package com.sitdh.master.eaa.main;

public class B6CoordinatePoint {
	
	public int x, y;
	
	public B6CoordinatePoint(int x, int y) {
		this.x = x;
		this.y = y;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

}
